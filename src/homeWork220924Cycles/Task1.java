package homeWork220924Cycles;

public class Task1 {
    /** Пользователь вводит два целых числа.
     * Выведите меньшее из них.
     */
    public static void main(String[] args) {
        int firstInteger = 4;
        int secondInteger = 3;
        if (firstInteger < secondInteger) {
            System.out.println(firstInteger);
        } else {
            System.out.println(secondInteger);
        }
    }
}

